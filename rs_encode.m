function code = rs_encode(msg)
% RS(7,3) 编码函数 — 系统生成矩阵编码
%
% RS(7,3) 码参数：
%   码长 n = 7, 消息长度 k = 3
%   最小符号距离 d_min = n - k + 1 = 5
%   可纠正 t = floor((d_min-1)/2) = 2 个符号错误
%
% 编码方式：
%   使用系统生成矩阵 G = [I_k | P]，其中 I_k 是 k×k 单位矩阵，
%   P 是 k×(n-k) 校验矩阵。
%   编码计算：codeword = msg * G （GF(2^3) 上的矩阵乘法）
%   结果前 k=3 列是原始消息符号，后 n-k=4 列是校验符号。
%
% 输入：
%   msg  - 待编码的消息，GF(2^3) 域，大小为 N×3
%          每行包含 3 个消息符号（每个符号取值 0~7）
%
% 输出：
%   code - 编码后的 RS(7,3) 码字，GF(2^3) 域，大小为 N×7
%          每行包含 7 个码字符号（前 3 个为原始消息，后 4 个为校验位）
%
% 示例：
%   msg = gf([1 2 3; 4 5 6], 3);   % 两个消息
%   code = rs_encode(msg);          % 两个编码后的码字 (2×7)

    % === 输入验证 ===
    if isempty(msg)
        error('rs_encode: 输入 msg 不能为空');
    end

    if ~isa(msg, 'gf') || msg.m ~= 3
        error('rs_encode: 输入 msg 必须是 GF(2^3) 域中的 gf 对象。请使用 gf(msg, 3) 进行转换');
    end

    if size(msg, 2) ~= 3
        error('rs_encode: 输入 msg 必须恰好有 3 列 (k=3)，当前列数为 %d', size(msg, 2));
    end

    % === 系统生成矩阵 G = [I_3 | P] ===
    % P 矩阵的系数由 RS(7,3) 码的生成多项式导出。
    % GF(2^3) 使用默认本原多项式 (十进制 11，即 x^3 + x + 1)。
    %
    % G 矩阵的每一行对应一个消息符号，每一列对应一个码字符号：
    %   列 1~3：单位矩阵（消息符号直接通过）
    %   列 4~7：校验矩阵 P（生成多项式系数经过系统化约简得到）

    G = gf([
        1 0 0 3 2 1 3
        0 1 0 5 5 1 4
        0 0 1 7 6 1 6
    ], 3);

    % === 编码：消息乘以生成矩阵 ===
    code = msg * G;

end
